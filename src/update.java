    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        // TODO add your handling code here:
        String pass = password.getText();
        String repass = repassword.getText();
        String cname = compname.getText();
        String user = account.getText();
        if (user != null && pass.equals(repass)){
            try{
                conn = DriverManager.getConnection("jdbc:derby://localhost:1527/bactong","bactong","bactong");
                st = conn.createStatement();
                st.executeUpdate("UPDATE BACTONG.LOGIN SET PASSWORD = '"+pass+"', CNAME = '"+cname+"' WHERE USERNAME = '"+user+"'");
                final JPanel panel = new JPanel();
                JOptionPane.showMessageDialog(panel,"Account Registered","Registration Notice", JOptionPane.INFORMATION_MESSAGE);
                String msg = account.getText();
                dispose();
                home info = new home(msg);
                info.setVisible(true);
            }
            catch (Exception ex){
                Logger.getLogger(login.class.getName()).log(Level.SEVERE,null,ex);
                final JPanel panel = new JPanel();
                JOptionPane.showMessageDialog(panel,"Username Invalid or Already Taken","Registration System", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            final JPanel panel = new JPanel();
            JOptionPane.showMessageDialog(panel,"Retype Password Doesn't Match!!","Registration System", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_createActionPerformed

